// Define the numbers and the operation here
let num1 = 5
let num2 = 4
let operation = "/"

// Write if-statements here

if (operation === "+") console.log(num1 + num2)
else if (operation === "-") console.log(num1 - num2)
else if (operation === "/") console.log(num1 / num2)
else if (operation === "*") console.log(num1 * num2)
else console.log("you can only add, multiply, subtract, or divide")